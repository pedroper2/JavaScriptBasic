//Operadores logicos 
let nome1 = "Mara"
let nome2 = "Mara"

let idade1 = 10 
let idade2 = '10'

console.log("IGUAL")
console.log(nome1 === nome2)
console.log(idade1 == idade2)
console.log(idade1 === idade2)

console.log("DIFERENTE")
console.log(nome1 != nome2)
console.log(idade1 !== idade2)


console.log("AND")
console.log(1 < 2 && 1 < 3)
console.log(10 < 2 && 1 < 3)

console.log("OR")
console.log(10 < 2 || 1 < 3)